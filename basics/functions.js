/**
 * Prints a greeting with the user's name.
 *
 * @param {string} name - The name of the user.
 * @param {string} greeting - Greeting type for user (Hello, Hi, Bye, Goodbye, etc.)
 * @returns {void}
 */
function greet(name, greeting) {
  console.log(`${greeting}, ${name}!`);
}

greet("syk", "hi");
